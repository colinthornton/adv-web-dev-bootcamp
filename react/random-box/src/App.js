import React, { Component } from 'react';
import './App.css';

class App extends Component {

  constructor(props) {
    super(props);
    this.state = {
      backgroundColor: props.allColors[getRandomInt(0, props.allColors.length)],
    }

    this.getNewColor = this.getNewColor.bind(this);
  }

  getNewColor() {
    this.setState({ backgroundColor: this.props.allColors[getRandomInt(0, this.props.allColors.length)] });
  }

  render() {
    const styles = {
      backgroundColor: this.state.backgroundColor,
      fontSize: `${getRandomInt(20, 101)}px`,
      color: 'white',
      textShadow: 'black 1px 1px 1px',
    }

    return (
      <div className="App"
           style={styles}
           onClick={this.getNewColor}
      >
        {this.state.backgroundColor}
      </div>
    );
  }
}

